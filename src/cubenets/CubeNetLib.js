function loadData() {
    let list = [];
    // valid
    validNetList.forEach(para => {
        for (let rnum = 0; rnum < 4; ++rnum) {
            list.push(new CubeNet(true, para.mat, false, rnum));
        }
        if (para.flipFlag) {
            for (let rnum = 0; rnum < 4; ++rnum) {
                list.push(new CubeNet(true, para.mat, true, rnum));
            }
        }
    });
    // invalid
    invalidNetList.forEach(para => {
        for (let rnum = 0; rnum < 4; ++rnum) {
            list.push(new CubeNet(false, para.mat, false, rnum));
        }
        if (para.flipFlag) {
            for (let rnum = 0; rnum < 4; ++rnum) {
                list.push(new CubeNet(false, para.mat, true, rnum));
            }
        }
    });
    return list;
}

export const CUBE_NET_LIB = loadData();